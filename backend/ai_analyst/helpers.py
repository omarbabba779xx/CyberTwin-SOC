"""Stateless helpers shared across the AI analyst mixins."""

from __future__ import annotations


def _is_private_ip(ip: str) -> bool:
    """Return True if *ip* is RFC 1918 / loopback."""
    parts = ip.split(".")
    if len(parts) != 4:
        return False
    try:
        octets = [int(p) for p in parts]
    except ValueError:
        return False
    if octets[0] == 10:
        return True
    if octets[0] == 172 and 16 <= octets[1] <= 31:
        return True
    if octets[0] == 192 and octets[1] == 168:
        return True
    if octets[0] == 127:
        return True
    return False
